import math

n1 = float(input("Ingrese un número: "))

seno = math.sin(n1)
coseno = math.cos(n1)
tangente = math.tan(n1)
raiz1 = math.sqrt(n1)
logaritmo_natural = math.log(n1)

print("Del número", n1)
print("Su seno es:", seno)
print("Su coseno es:", coseno)
print("Su tangente es:", tangente)
print("Su raíz cuadrada es:", raiz1)
print("Su logaritmo natural es:", logaritmo_natural)
	